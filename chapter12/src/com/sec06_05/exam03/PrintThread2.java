package com.sec06_05.exam03;

public class PrintThread2 extends Thread {	//무한 반복해서 출력하는 스레드
	public void run() {
		while(true) {
			System.out.println("실행 중");
			if(Thread.interrupted()) {	//혹은 (new Thread()).isInterrupted();
				break;	//while문을 빠져나옴
			}
		}
		
		System.out.println("자원 정리");
		System.out.println("실행 종료");
	}
}
